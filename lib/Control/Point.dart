 class Point {
   int x=0;
   int y=0;
   String color="green";
   Point(int x,int y,String color){this.x=x;this.y=y;this.color=color;}


   int getX() {
    return x;
  }

   int getY() {
    return y;
  }

   void setX(int x) {
    this.x = x;
  }

   void setY(int y) {
    this.y = y;
  }
}
